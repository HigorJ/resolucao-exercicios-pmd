const operacoes = {
    soma: (x, y) => x + y,
    subtracao: (x, y) => x - y,
    multiplicacao: (x, y) => x * y,
    divisao: (x, y) => x / y
}

console.log(operacoes.soma(5, 12));
console.log(operacoes.subtracao(90, 45));
console.log(operacoes.multiplicacao(4, 5));
console.log(operacoes.divisao(50, 5));