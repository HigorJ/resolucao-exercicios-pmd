function soma(x, y) {
    return x + y;
}

function subtracao(x, y) {
    return x - y;
}

function multiplicacao(x, y) {
    return x * y;
}

function divisao(x, y) {
    return x / y;
}

console.log(soma(4, 8));
console.log(subtracao(10, 7));
console.log(multiplicacao(6, 10));
console.log(divisao(16, 4));
